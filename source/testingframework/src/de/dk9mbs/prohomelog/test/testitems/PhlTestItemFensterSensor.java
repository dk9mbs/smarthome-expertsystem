    private void _publish() {
        String off=this.getProp("item_value_off", "off");
        String on=this.getProp("item_value_on", "on");
        this.jCheckBox1.setText(on);
        String value=off;
        
        if (this.jCheckBox1.isSelected()) value=on;
        this.getPublisher().publish(this.getProp("broker_pub_topic", "dummy/dummy") , value);        
    }
    
