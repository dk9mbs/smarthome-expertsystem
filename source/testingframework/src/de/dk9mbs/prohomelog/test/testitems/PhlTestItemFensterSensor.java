    @Override
    public void szenarioTick(int tick) {
        if(de.dk9mbs.prohomelog.test.PropTools.existItem(this.getProp("time_on", ""), String.valueOf(tick) )) {
            this.jCheckBox1.setSelected(true);
            this._publish();
        }

        if(de.dk9mbs.prohomelog.test.PropTools.existItem(this.getProp("time_off", ""), String.valueOf(tick) )) {
            this.jCheckBox1.setSelected(false);
            this._publish();
        }
    }
    
